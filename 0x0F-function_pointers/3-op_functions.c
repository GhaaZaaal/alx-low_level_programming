#include "3-calc.h"

/**
 * op_add - C func that adds two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - C func that subtract two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the subtraction
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - C func that multiplies two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the multiplication
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - C func that divided two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the division
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - C func that module of two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the modulous
*/

int op_mod(int a, int b)
{
	return (a % b);
}

